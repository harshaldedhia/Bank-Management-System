    public void getname(){
        String sql="select * from BalanceInfo where AccNum=?";
        try{
        pst=conn.prepareStatement(sql);
        pst.setString(1, num);
        rs=pst.executeQuery();
        if(rs.next()){
            String name=rs.getString("Name");
            String bal=rs.getString("Balance");
            rs.close();
            pst.close();
            jTextField1.setText(name);
            jTextField3.setText(bal);
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
    }
    finally{
            try{
            rs.close();
            pst.close();
        }catch(Exception e){
                }
        }
    }
    
    public void UpdateBal(float n,String bal){
        try{
            float sum=Float.parseFloat(bal)+n;
            String sql="update BalanceInfo set Balance='"+String.valueOf(sum)+"' where AccNum='"+jTextField4.getText()+"'";
            pst=conn.prepareStatement(sql);
            pst.execute();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
    }

