        String bal;
        String sql="select * from BalanceInfo where AccNum=?";
        try{
        pst=conn.prepareStatement(sql);
        pst.setString(1, jTextField4.getText());
        rs=pst.executeQuery();
        if(rs.next()){
            bal=rs.getString("Balance");
            rs.close();
            pst.close();
    
        float a1=Float.parseFloat(jTextField3.getText());
        float a2=Float.parseFloat(jTextField5.getText());
        try{
            if(a1>=a2){
                float diff=a1-a2;
                String sql1="update BalanceInfo set Balance='"+String.valueOf(diff)+"' where AccNum='"+num+"'";
                pst=conn.prepareStatement(sql1);
                pst.execute();
            }
            else{
                JOptionPane.showMessageDialog(null,"Insufficient Balance");
                jTextField5.setText("");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        UpdateBal(a2,bal);
        JOptionPane.showMessageDialog(null,"Amount Transferred Successfully!!!");
        new Home(num).setVisible(true);
        this.setVisible(false);
        }
        
        else{
                JOptionPane.showMessageDialog(null,"Account Doesnot Exist");
                }
        }catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
    }
